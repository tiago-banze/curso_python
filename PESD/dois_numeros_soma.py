# usuário e exiba a soma, subtracção, multiplicação e 
# divisão entre eles.

n1 = int(input("digite um valor: "))
n2 = int(input("digete segundo valor: "))

print(f'{n1} + {n2} = {n1+n2}')
# print(n1, '+', n2, '=', n1+n2)
print(f'{n1} - {n2} = {n1-n2}')
print(f'{n1} * {n2} = {n1*n2}')
print(f'{n1} / {n2} = {n1//n2}')